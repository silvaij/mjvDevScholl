package transmissao;

public class Contato {
    String nome;
    Long Telefone;

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public Long getTelefone() {
        return Telefone;
    }

    public void setTelefone(long telefome) {
        Telefone = telefome;
    }
}
